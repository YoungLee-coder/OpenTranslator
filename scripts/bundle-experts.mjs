/**
 * Parse Immersive Translate expert YAML plugins into a bundled TS module
 * consumed by the Worker at runtime (no filesystem reads in production).
 */
import { parse } from "yaml";
import { readdirSync, readFileSync, writeFileSync } from "node:fs";
import { join, dirname } from "node:path";
import { fileURLToPath } from "node:url";

const __dirname = dirname(fileURLToPath(import.meta.url));
const pluginsDir = join(__dirname, "../src/experts/plugins");
const outFile = join(__dirname, "../src/experts/bundled.ts");

/** Pick highest-version .add_v.[x.y.z] override for a base field. */
function resolveVersionedField(doc, base) {
  const addPrefix = `${base}.add_v.[`;
  let best = null;
  let bestVer = "";
  for (const [key, val] of Object.entries(doc)) {
    if (key.startsWith(addPrefix) && typeof val === "string" && val.trim()) {
      const ver = key.slice(addPrefix.length, -1);
      if (ver > bestVer) {
        bestVer = ver;
        best = val;
      }
    }
  }
  if (best) return best;
  const baseVal = doc[base];
  if (typeof baseVal === "string" && baseVal.trim()) return baseVal;
  // YAML typo key "multiplePrompt:" from "multiplePrompt::"
  const typoKey = `${base}:`;
  const typoVal = doc[typoKey];
  if (typeof typoVal === "string" && typoVal.trim()) return typoVal;
  return undefined;
}

function normalizeExpert(raw) {
  const id = raw.id;
  if (!id || typeof id !== "string") return null;

  const removeKeys = Object.keys(raw).filter((k) => k.includes(".remove_v.["));
  const removed = new Set(removeKeys.map((k) => k.split(".remove_v.[")[0]));

  function pick(field) {
    if (removed.has(field)) return undefined;
    return resolveVersionedField(raw, field);
  }

  return {
    id,
    version: String(raw.version ?? "1.0.0"),
    name: String(raw.name ?? id),
    description: String(raw.description ?? ""),
    avatar: raw.avatar ? String(raw.avatar) : undefined,
    author: raw.author ? String(raw.author) : undefined,
    homepage: raw.homepage ? String(raw.homepage) : undefined,
    details: raw.details ? String(raw.details) : undefined,
    i18n: raw.i18n ?? undefined,
    env: raw.env ?? {},
    systemPrompt: pick("systemPrompt"),
    multipleSystemPrompt: pick("multipleSystemPrompt"),
    prompt: pick("prompt"),
    multiplePrompt: pick("multiplePrompt"),
    langOverrides: Array.isArray(raw.langOverrides) ? raw.langOverrides : [],
  };
}

const files = readdirSync(pluginsDir).filter((f) => f.endsWith(".yml"));
const experts = files
  .map((f) => {
    const raw = parse(readFileSync(join(pluginsDir, f), "utf8"));
    return normalizeExpert(raw);
  })
  .filter(Boolean)
  .sort((a, b) => a.id.localeCompare(b.id));

writeFileSync(
  outFile,
  `/** Auto-generated by scripts/bundle-experts.mjs — do not edit. */\n` +
    `import type { AiExpertDefinition } from "./types";\n\n` +
    `export const bundledExperts: AiExpertDefinition[] = ${JSON.stringify(experts, null, 2)};\n`,
);

console.log(`Bundled ${experts.length} experts → ${outFile}`);
